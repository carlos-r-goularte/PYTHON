#funções 
# any()- retorna True se algum valor for verdade
# all()- retorna True se todos os valores forem verdade

lista = [1,2,3,4,5]
print(any(lista))
print(all(lista))

lista2 = [0,1,2,3,4]
print(any(lista2))
print(all(lista2))

lista3 = [0,0,0,0,0]
print(any(lista3))
print(all(lista3))

# min() - retorna o valor mínimo da lista
# max() - retorna o valor máximo da lista
# sum() - soma de todos os valores de uma lista

print(min(lista))
print(max(lista))
print(sum(lista))